#!/usr/bin/env node

import { execSync } from 'node:child_process';
import { mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import path from 'node:path';

const repoRoot = process.cwd();
const specsDir = path.join(repoRoot, 'specs');
const currentWorkPath = path.join(specsDir, 'CURRENT-WORK.md');
const reconciliationPath = path.join(specsDir, 'RECONCILIATION.md');
const implementationPlanPath = path.join(repoRoot, 'IMPLEMENTATION_PLAN.md');

function run(command) {
  return execSync(command, {
    cwd: repoRoot,
    encoding: 'utf8',
    stdio: ['ignore', 'pipe', 'pipe'],
  }).trim();
}

function safeRun(command, fallback = '') {
  try {
    return run(command);
  } catch {
    return fallback;
  }
}

function getNextUncheckedPlanItem() {
  const planText = readFileSync(implementationPlanPath, 'utf8');
  const line = planText
    .split(/\r?\n/u)
    .find((entry) => entry.trimStart().startsWith('- [ ] **'));

  if (!line) {
    return 'No unchecked implementation-plan items found.';
  }

  return line.trim().replace(/^- \[ \]\s*/u, '');
}

function getOpenItemCount() {
  const planText = readFileSync(implementationPlanPath, 'utf8');
  return planText
    .split(/\r?\n/u)
    .filter((entry) => entry.trimStart().startsWith('- [ ] **')).length;
}

function getWorktreeLines() {
  const raw = safeRun('git status --short --untracked-files=all', '');
  return raw
    .split(/\r?\n/u)
    .map((line) => line.trimEnd())
    .filter(Boolean)
    .filter((line) => !line.includes('specs/CURRENT-WORK.md'))
    .filter((line) => !line.includes('specs/RECONCILIATION.md'))
    .sort((left, right) => left.localeCompare(right));
}

function buildCurrentWork() {
  const branch = safeRun('git branch --show-current', '(unknown)');
  const head = safeRun('git rev-parse --short HEAD', '(unknown)');
  const worktreeLines = getWorktreeLines();
  const nextItem = getNextUncheckedPlanItem();

  const worktreeSection =
    worktreeLines.length === 0
      ? '- Clean worktree (excluding continuity files).'
      : worktreeLines.map((line) => `- ${line}`).join('\n');

  return `# Current Work

Generated by \`npm run continuity:update\`.

## Repository
- Branch: \`${branch}\`
- HEAD: \`${head}\`

## Next Plan Item
- ${nextItem}

## Working Tree
${worktreeSection}
`;
}

function buildReconciliation() {
  const branch = safeRun('git branch --show-current', '(unknown)');
  const head = safeRun('git rev-parse HEAD', '(unknown)');
  const openItems = getOpenItemCount();
  const changedFiles = getWorktreeLines().length;

  return `# Reconciliation

Generated by \`npm run continuity:update\`.

## Snapshot
- Branch: \`${branch}\`
- HEAD: \`${head}\`
- Open implementation-plan items: ${openItems}
- Non-continuity working-tree entries: ${changedFiles}

## Intent
- Keep a lightweight, committed continuity snapshot in the repo.
- Fail validation when this summary needs review and commit.
`;
}

mkdirSync(specsDir, { recursive: true });
writeFileSync(currentWorkPath, buildCurrentWork(), 'utf8');
writeFileSync(reconciliationPath, buildReconciliation(), 'utf8');
